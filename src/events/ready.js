module.exports = {
  name: Events.ClientReady,
  once: true,
  execute(client) {
    client.user.setActivity("Maplestory | /help");

    mongoose.connect(process.env.MONGO_URI);

    console.log(`Ready! Logged in as ${client.user.tag}`);
  },
};
